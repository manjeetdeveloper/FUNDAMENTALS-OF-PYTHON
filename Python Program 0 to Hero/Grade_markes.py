marks = int(input("Enter your Marks"))
print("Your marks is : ", marks)

if (marks >= 90):
    print("Your Grade is 'A' ")

elif(marks >= 75):
    print("Your Grade is 'B' ")

elif(marks >= 50):
    print("Your Grade is 'c' ")

elif(marks >= 40):
    print("Your Grade is 'D' ")

else:
    print("Your Grade id E and You are Fail")